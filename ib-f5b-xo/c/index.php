		<p><strong>1. Cash in on Social Media</strong><br />
Social media is hot, hot, hot. Be among the first to friend or fan a company on Facebook, and you could be rewarded with a gift card. Get involved in Twitter parties for even more chances to win.</p>
<p><strong> 2. Transfer Your Prescriptions</strong><br />
Pharmacies want your business, and they&#8217;re willing to fight for it. Flip through weekly sales circulars in search of pharmacy coupons that can be redeemed for $25 gift cards (or more) when you transfer your prescriptions. With pharmacies popping up all over the place, you should be able to earn several gift cards each year, simply by sharing the refill wealth.</p>
<p><strong> 3. Sign Up for MyPoints</strong><br />
Earn points redeemable for gift cards by reading e-mails, answering surveys, doing web searches and shopping through Mypoints.com. There are over 75 gift cards to choose from, and they all ship no charge.</p>
<p><strong>4. Sign Up for Swagbucks</strong><br />
<span style="line-height: 1.5;">Swagbucks is another online rewards program where you can earn gift cards for completing tasks like answering surveys, printing coupons, shopping and searching the web. Sign up for both, and max out your earnings potential.</span></p>
<p><strong>5. Answer Surveys</strong><br />
Become a member of Harris Poll Online and convert your opinions into free gift cards by filling out online surveys in your free time.</p>
<p><strong>6. Test Drive Cars</strong><br />
Auto dealers frequently offer free gift cards for test driving a particular vehicle. Take a quick spin, and walk away with a fab freebie.</p>
<p><strong>7. Cash in Your Credit Card Rewards</strong><br />
If you have the discipline (and means) to pay off your credit card each month, charging your day-to-day purchases to a rewards card can be a great way to earn free gift cards fast.</p>
<p><strong> 8. Rebate</strong><br />
Food companies frequently run mail-in promotions to reward customers. Spend a certain dollar amount (or buy a certain amount of product), and you could be eligible for a free gift card. The cereal aisle is a great place to look for these types of deals.</p>
<p><strong> 9. Enter Sweepstakes and Instant Win Games</strong><br />
Set aside 15 minutes each day to enter sweepstakes and instant win games that offer gift cards as a prize. The odds of winning are better than you might think.</p>
